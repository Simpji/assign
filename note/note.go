// `Math.floor()` is a function that rounds a number down to the nearest integer. This means that any decimal part of the number is truncated, and the resulting number is always an integer.

// For example:

// - `Math.floor(3.7)` would return 3
// - `Math.floor(2.2)` would return 2
// - `Math.floor(5.9)` would return 5

